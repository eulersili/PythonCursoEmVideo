n = int(input('Digite um valor: '))
d = n * 2
t = n * 3
r = n ** (1/2)
print('O dobro de {} vale {}'.format(n, d))
print('O triplo de {} vale {}. \nA raiz quadrada de {} é igual a {}'.format(n, t, n, r))

# O "\n", serve para que tudo depois dele, fique na outra linha.
